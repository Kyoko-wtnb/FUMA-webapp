<div id="data-security" class="sidePanel container" style="padding-top:50px;">
	<h2>Data Security</h2>
	<div style="margin-left: 40px;">
		<h3>GWAS summary statistics</h3>
		<span style="font-size:18px;">
			User uploaded input file (GWAS summary statistics) is stored into the server until the SNP2GENE process is done.
			<span style="color:blue;">Once the process is done, the input file is completely deleted. </span>
			There is no backup for user uploaded files.
		</span>
		<h3>FUMA results</h3>
		<span style="font-size:18px;">
			FUMA results are stored into the server, however only logged in users can access to their own data.
			<span style="color:blue;">When users delete jobs, all results including the job submission recodes will be completely removed from the server.</span>
			Therefore, it is no longer possible to recover jobs which are accidentally removed.
			We recommend to download results (text files) and images as much as possible before deleting the job.
			<span style="color:red;">Even though, FUMA results are only accessible from the logged in users, we strongly recommend to remove jobs whose results need to be highly secured.</span>
		</span>
	</div>
</div>
